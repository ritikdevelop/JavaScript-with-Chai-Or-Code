console.log(Math.ceil(a));
// Output: 10

// * Math.floor()

console.log(Math.floor(a));
// Output: 10

// * Math.round()1

console.log(Math.round(a));
// Output: 10

// * Math.max()

console.log(Math.max(1, 2, 3, 4, 5));
// Output: 5

// * Math.min()

console.log(Math.min(1, 2, 3, 4, 5));
// Output: 1

