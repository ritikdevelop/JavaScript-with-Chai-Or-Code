//! There are 3 types of logical Operation in JavaScript

// Logical AND (&&)
// Logical OR (||)
// Logical NOT (!)

// * Logical AND

let a = 10;
let b = 20;

console.log(a > 10 && b > 10); // false

// * Logical OR

console.log(a > 10 || b > 10); // true

// * Logical NOT

console.log(!(a > 10)); // false
