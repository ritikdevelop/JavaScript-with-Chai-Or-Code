// ! There are 8 types of comparison operators in JavaScript

// 1. Equal to (==)
// 2. Strict equal to (===)
// 3. Not equal to (!=)
// 4. Strict not equal to (!==)
// 5. Greater than (>)
// 6. Less than (<)
// 7. Greater than or equal to (>=)
// 8. Less than or equal to (<=)

// todo The comparison operators are used to compare two values and return a boolean value (true or false)

// * Equal to
console.log(1 == 1); // true
console.log(1 == 2); // false

// * Strict equal to
console.log(1 === 1); // true
console.log(1 === 2); // false

// * Not equal to
console.log(1 != 1); // false
console.log(1 != 2); // true

// * Strict not equal to
console.log(1 !== 1); // false
console.log(1 !== 2); // true

// * Greater than
console.log(1 > 1); // false
console.log(1 > 2); // false

// * Less than
console.log(1 < 1); // false
console.log(1 < 2); // true

// * Greater than or equal to
console.log(1 >= 1); // true
console.log(1 >= 2); // false

// * Less than or equal to
console.log(1 <= 1); // true
console.log(1 <= 2); // true

