// ? Conversion in javaScript

// 1. Number to String

let num = 123;

let str = String(num);

console.log(str);
console.log(typeof str);
// Output: string

// 2. String to Number

let str2 = "123";

let num2 = Number(str2);

console.log(num2);
console.log(typeof num2);
// Output: number


// ! Operation in JavaScript
// Addition (+)
// Subtraction (-)
// Multiplication (*)
// Division (/)
// Modulus (%)
// Increment (++)
// Decrement (--)

//* Addition 

let a = 5;
let b = 10;

let sum = a + b;
console.log(sum);
// Output = 15

// * Subtraction

let sub = b - a;
console.log(sub);
// Output = 5

// * Multiplication

let mul = a * b;
console.log(mul);
// Output = 50

// * Division

let div = b / a;
console.log(div);
// Output = 2.5

// * Modulus

let mod = b % a;
console.log(mod);
// Output = 0

// * Increment

let inc = a++;
console.log(inc);
// Output = 5

// * Decrement

let dec = a--;
console.log(dec);
// Output = 4
